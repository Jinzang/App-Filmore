=head1 NAME

App::Filmore - Server side support for CGI scripting 

=head1 SYNOPSIS

    use App::Filmore;

=head1 DESCRIPTION

App::Filmore is a collection of modules used to support cgi scripting on
a web server. It will be bundled with several example script, the first of
which is search.cgi.

